number1 = int(input("Введите число1 "))# Просим ввести первое число с клавиатуры
number2 = int(input("Введите число2 "))# Просим ввести второе число с клавиатуры

print("Число", number1, "= числа", number2) if number1 == number2 else print("Меньшее число - ", min(number1, number2))# Выводим "=", если числа равны или выводим меньшее число

